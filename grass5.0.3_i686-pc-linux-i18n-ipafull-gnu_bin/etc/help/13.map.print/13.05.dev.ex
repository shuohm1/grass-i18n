                          SUPPORTED PRINTERS

This section lists the printers that have been currently tested and
are supported.  In future releases of GRASS, plans are to add any
drivers that users have written for specific printers and include
them in an unsuppported printers section (if they are not able to be
fully tested). If you have written a device driver or are using a
printer that is not listed here, please contact Doug Brooks
(217-373-6752).  He would like to include your drivers in this section.


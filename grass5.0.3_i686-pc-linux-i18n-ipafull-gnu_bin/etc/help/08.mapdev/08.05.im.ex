           IMPORTING AND EXPORTING GRASS DATA TO OTHER FORMATS

Often, data which one wishes to use in a GRASS database are already
available in digital data formats other than GRASS.  Similarly, users
often wish to export data from GRASS into other software systems' data
formats.  A number of GRASS programs are available to import and export
data to and from GRASS.  These are listed below.  Prefixes indicate
whether commands operate on vector ("v.") or raster ("r.") data.

       \-  v.import \            \-  v.support \
       \-  v.in.ascii \          \-  v.out.ascii \
       \-  v.in.arc \            \-  v.out.arc \
       \-  v.in.dlg \            \-  v.out.dlg \
       \-  v.in.dxf \            \-  v.out.dxf \
       \-  v.in.tiger \          \-  v.out.moss \
       \-  Gen.Maps \
       \-  Gen.tractmap \

       \-  r.in.ascii \          \-  r.out.ascii \
       \-  r.in.ll \
       \-  r.in.sunrast \


For more information on a specific command, select a command and press
ESC.






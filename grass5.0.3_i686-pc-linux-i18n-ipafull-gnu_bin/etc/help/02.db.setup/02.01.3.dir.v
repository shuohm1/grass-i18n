  GRASS VECTOR DATA DIRECTORIES:

  dig_ascii:  Contains ASCII files in GRASS vector (dig) format.
  dig:        Contains binary files in GRASS vector (dig) format.
  dig_plus:   Contains topology information for files in GRASS vector
                (dig) format.
  dig_att:    Contains the locations at which category labels will be
                placed for files in GRASS vector (dig) format.
  dig_cats:   Contains the category text labels used
                for files in GRASS vector (dig) format.
  reg:        Contains the geographic coordinates of points used to
                register to a known coordinate system a (vector) map
                layer digitized with the GRASS "digit" program.


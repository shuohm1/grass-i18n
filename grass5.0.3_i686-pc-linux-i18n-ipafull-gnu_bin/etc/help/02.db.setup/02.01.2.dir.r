  GRASS RASTER DATA DIRECTORIES:

  cell:      Contains data files in GRASS raster format.
  cellhd:    Contains header information for data files in GRASS raster
               format.
  cell_misc: Contains histogram and range data for GRASS raster files.
  cats:      Contains category value and label information for files in
               GRASS raster format.
  hist:      Contains history files noting how GRASS raster map layers
               were created.
  colr:      Contains color table files for raster map layers in the
               user's current mapset.
  colr2:     Contains color table files modified by the user for raster
               map layers NOT in the user's current mapset.

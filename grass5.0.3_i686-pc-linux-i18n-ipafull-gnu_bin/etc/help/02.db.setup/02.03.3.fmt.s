What Are Site Data?

Site data are point data.  The spatial location of each site is given as
an x,y coordinate pair.  \Category\ values, labels, and graphic icons may
also be associated with sites.  In GRASS, site data are stored in ASCII
files under the "site_lists" directory under each mapset.  

Site data can be analyzed in GRASS either in their site_lists file
format or after conversion to raster data format.

Read the information under "GRASS Database Directory Structure," located
back at the menu, if you wish further information on the location and
content of GRASS site_lists files.

